    private void jbEnviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbEnviarMouseClicked
        // TODO add your handling code here
        
        int escolha = Integer.parseInt(jtAdivinha.getText());
        
        verificarAdivinha(escolha, numero);
    }//GEN-LAST:event_jbEnviarMouseClicked

    private void jtAdivinhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtAdivinhaKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            int escolha = Integer.parseInt(jtAdivinha.getText());
            verificarAdivinha(escolha, numero);
            limparCampo();
            tentativas();
        }

    }//GEN-LAST:event_jtAdivinhaKeyPressed

