    public void tentativas(){
        tentativas++;
        jlTentativas.setText("Você já realizou "+tentativas+" tentativas");
    }
    
    public int sorteia(){
        Random random = new Random();
        numero = random.nextInt(11);
        
        return numero;
    }
    
    private void limparCampo(){
        jtAdivinha.setText("");
    }
    
    
    private void resetarJogo(){
        jlCima.setText("JOGO DA SORTE");
        jlBaixo.setText("DIGITE UM NÚMERO PARA TENTAR A SORTE");
        tentativas = 0;
        limparCampo();
    }
    
    private void ganhouJogo(){
        JOptionPane.showMessageDialog(this, "Parabens voce ganhou o jogo");
        resetarJogo();
    }
    private void verificarAdivinha(int escolha, int numero){
        if (escolha == numero) {
                ganhouJogo();
            } else if (escolha > numero) {
                jlBaixo.setText("DIGITE OUTRO NÚMERO PARA TENTAR A SORTE");
                jlCima.setText("O NUMERO ESCOLHIDO É MAIOR QUE O NUMERO DA SORTE");
            } else {
                jlBaixo.setText("DIGITE OUTRO NÚMERO PARA TENTAR A SORTE");
                jlCima.setText("O NUMERO ESCOLHIDO É MENOR QUE O NUMERO DA SORTE");
            } 
        limparCampo();
    }
    
